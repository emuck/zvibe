"""
z3_to_c_array.py - Convert Z-Machine story file to C array for embedded use

This script converts a .z3 story file into a C source file containing
the story data as a const uint8_t array, suitable for embedding in flash memory.

Usage: python3 z3_to_c_array.py input.z3 output.c
"""

import sys
import os
from pathlib import Path

def convert_z3_to_c_array(input_file, output_file, array_name=None, single_game=False):
    """Convert Z3 file to C array format"""
    
    try:
        # Read the binary Z3 file
        with open(input_file, 'rb') as f:
            data = f.read()
        
        # Get file info
        file_size = len(data)
        input_name = Path(input_file).stem
        
        # Generate array name from filename if not provided
        if array_name is None:
            if single_game:
                # Use generic names for single-game mode
                array_name = 'story'
            else:
                # Convert filename to valid C identifier (multi-game mode)
                array_name = input_name.lower().replace('-', '_').replace(' ', '_')
                # Remove any non-alphanumeric characters except underscores
                array_name = ''.join(c for c in array_name if c.isalnum() or c == '_')
                if not array_name[0].isalpha() and array_name[0] != '_':
                    array_name = 'game_' + array_name
        
        print(f"Converting {input_file} ({file_size} bytes) to {output_file}")
        print(f"Array name: {array_name}_data")
        
        # Generate C source file
        with open(output_file, 'w') as f:
            f.write(f"""/*
 * Generated story data from {os.path.basename(input_file)}
 * File size: {file_size} bytes
 * Generated by: z3_to_c_array.py
 */

#include <stdint.h>
#include <stddef.h>

/* Story data array - stored in flash memory */
const uint8_t {array_name}_data[] = {{
""")
            
            # Write data as hex bytes, 16 per line
            for i in range(0, file_size, 16):
                f.write("    ")
                for j in range(16):
                    if i + j < file_size:
                        f.write(f"0x{data[i + j]:02x}")
                        if i + j < file_size - 1:
                            f.write(", ")
                    else:
                        break
                f.write("\n")
            
            f.write(f"""
}};

/* Story data size */
const size_t {array_name}_data_size = {file_size};

/* Story name for reference */
const char {array_name}_name[] = "{input_name}";
""")
        
        print(f"Successfully generated {output_file}")
        return True
        
    except Exception as e:
        print(f"Error: {e}")
        return False

def main():
    import argparse
    
    parser = argparse.ArgumentParser(description='Convert Z-Machine story file to C array')
    parser.add_argument('input_file', help='Input .z3 file')
    parser.add_argument('output_file', help='Output .c file')
    parser.add_argument('array_name', nargs='?', help='Array name (optional)')
    parser.add_argument('--single-game', action='store_true', 
                        help='Generate generic story_data/story_data_size names for single-game mode')
    
    args = parser.parse_args()
    
    input_file = args.input_file
    output_file = args.output_file
    array_name = args.array_name
    single_game = args.single_game
    
    if not os.path.exists(input_file):
        print(f"Error: Input file '{input_file}' not found")
        sys.exit(1)
    
    if not input_file.lower().endswith('.z3'):
        print(f"Warning: Input file '{input_file}' doesn't have .z3 extension")
    
    if convert_z3_to_c_array(input_file, output_file, array_name, single_game):
        sys.exit(0)
    else:
        sys.exit(1)

if __name__ == "__main__":
    main()