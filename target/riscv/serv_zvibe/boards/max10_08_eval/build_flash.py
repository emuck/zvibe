"""Build MAX10 flash image — XIP direct boot, no bootloader.

Layout (all offsets computed from actual binary sizes):

  Physical    Size      Component
  ──────────────────────────────────────────
  0x000000    256 B     ZVIF metadata header
  0x000100    ~37 KB    Firmware  (reset_pc entry)
  toc_off     96 B      ZVGM TOC (header + 1 entry)
  data_off    ~126 KB   Game data
  saves_off   2KB x N   Delta-save slots (remaining space)

Usage:
  python3 build_flash.py --firmware <fw.bin> --game <game.z3> \\
                         --game-id <id> --game-name <name> --output <out.bin>
  python3 build_flash.py --firmware <fw.bin> --layout-only
"""

import argparse
import json
import sys
from pathlib import Path

# ---------------------------------------------------------------------------
# Shared ZVIF/ZVGM serialization library lives in common/sw/
# ---------------------------------------------------------------------------
sys.path.insert(0, str(Path(__file__).resolve().parents[2] / 'common' / 'sw'))
from zvibe_flash_lib import create_metadata, create_toc, METADATA_SIZE  # noqa: E402

# ---------------------------------------------------------------------------
# MAX10-specific constants
# ---------------------------------------------------------------------------
UFM_TOTAL       = 176128        # 172 KB on-chip flash
XIP_BASE        = 0x80000000    # CPU flash base address
DELTA_SLOT_SIZE = 2048          # Per-save slot size (bytes)


def calculate_layout(firmware_size, games_size=0, num_games=1):
    """Compute tight MAX10 layout from actual binary sizes."""
    layout = {}

    # Metadata — fixed 256 B at offset 0
    layout['metadata_offset'] = 0
    layout['metadata_size']   = METADATA_SIZE

    # Firmware starts at 0x100 (reset_pc = 0x80000100)
    fw_start    = 0x100
    fw_reserved = ((firmware_size + 255) // 256) * 256   # round up to 256-B page

    layout['firmware_offset']   = fw_start
    layout['firmware_size']     = firmware_size
    layout['firmware_reserved'] = fw_reserved
    layout['firmware_vaddr']    = XIP_BASE + fw_start

    # TOC immediately after firmware
    toc_start = fw_start + fw_reserved
    toc_size  = 16 + num_games * 80

    layout['toc_offset'] = toc_start
    layout['toc_size']   = toc_size

    # Game data immediately after TOC
    gd_start = toc_start + toc_size
    layout['games_data_offset'] = gd_start
    layout['games_data_vaddr']  = XIP_BASE + gd_start

    # Save slots fill whatever is left (page-aligned start) — allocated per game
    saves_start = ((gd_start + games_size + 2047) // 2048) * 2048
    remaining   = UFM_TOTAL - saves_start
    slot_count  = remaining // DELTA_SLOT_SIZE

    # Per-game saves: stored in TOC entry; global ZVIF saves fields are unused
    layout['saves_offset']     = 0
    layout['saves_size']       = 0
    layout['save_slot_size']   = DELTA_SLOT_SIZE
    layout['save_slot_count']  = 0
    layout['games_available']  = UFM_TOTAL - gd_start - slot_count * DELTA_SLOT_SIZE

    # Per-game save info (used when populating TOC entries)
    layout['per_game_saves_offset']     = saves_start if slot_count > 0 else 0
    layout['per_game_save_slot_count']  = slot_count

    layout['total_size'] = UFM_TOTAL
    layout['xip_base']   = XIP_BASE
    return layout


# ---------------------------------------------------------------------------
# Output helpers
# ---------------------------------------------------------------------------

def generate_flash_layout_h(layout, output_dir):
    """Write flash_layout.h consumed by firmware builds."""
    (output_dir / 'flash_layout.h').write_text(
        f"/* Auto-generated by boards/max10_08_eval/build_flash.py  DO NOT EDIT */\n"
        f"\n"
        f"#ifndef FLASH_LAYOUT_H\n"
        f"#define FLASH_LAYOUT_H\n"
        f"\n"
        f"/* Platform: MAX10   Total UFM: {layout['total_size']} bytes (172 KB) */\n"
        f"\n"
        f"/* Physical offsets */\n"
        f"#define FLASH_METADATA_OFFSET  0x{layout['metadata_offset']:08X}\n"
        f"#define FLASH_FIRMWARE_OFFSET  0x{layout['firmware_offset']:08X}\n"
        f"#define FLASH_GAMES_OFFSET     0x{layout['games_data_offset']:08X}\n"
        f"\n"
        f"/* XIP virtual addresses (CPU view) */\n"
        f"#define XIP_BASE               0x{layout['xip_base']:08X}\n"
        f"#define XIP_FIRMWARE_ADDR      0x{layout['firmware_vaddr']:08X}\n"
        f"#define XIP_GAMES_ADDR         0x{layout['games_data_vaddr']:08X}\n"
        f"\n"
        f"/* Capacity */\n"
        f"#define FIRMWARE_RESERVED      {layout['firmware_reserved']}\n"
        f"#define GAMES_AVAILABLE        {layout['games_available']}\n"
        f"\n"
        f"/* Save Region (per-game; offsets stored in TOC entries, not globally) */\n"
        f"#define FLASH_SAVES_OFFSET     0x{layout.get('per_game_saves_offset', 0):08X}\n"
        f"#define FLASH_SAVES_SIZE       {layout.get('per_game_save_slot_count', 0) * layout['save_slot_size']}\n"
        f"#define FLASH_SAVE_SLOT_SIZE   {layout['save_slot_size']}\n"
        f"#define FLASH_SAVE_SLOT_COUNT  {layout.get('per_game_save_slot_count', 0)}\n"
        f"\n"
        f"#endif /* FLASH_LAYOUT_H */\n")
    print(f"Generated: {output_dir / 'flash_layout.h'}")


def print_layout(layout, fw_size, gd_size, games):
    """Pretty-print the computed layout."""
    print()
    print('=' * 70)
    print('MAX10 FLASH LAYOUT')
    print('=' * 70)
    print(f"  Total:      {UFM_TOTAL:,} bytes (172 KB)")
    print()
    print(f"  Metadata:   {METADATA_SIZE:6,} B  @ 0x{0:06X}")
    print(f"  Firmware:   {fw_size:6,} B  @ 0x{layout['firmware_offset']:06X}"
          f"  (XIP 0x{layout['firmware_vaddr']:08X})")
    if games:
        print(f"  TOC:        {layout['toc_size']:6,} B  @ 0x{layout['toc_offset']:06X}"
              f"  ({len(games)} game(s))")
        print(f"  Games:      {gd_size:6,} B  @ 0x{layout['games_data_offset']:06X}"
              f"  (XIP 0x{layout['games_data_vaddr']:08X})")
        for g in games:
            print(f"                \"{g['name']}\"  ({len(g['data']):,} B)")
    else:
        print(f"  Games:      {layout['games_available']:6,} B  available")
    if layout.get('per_game_save_slot_count', 0) > 0:
        sc = layout['per_game_save_slot_count']
        so = layout['per_game_saves_offset']
        print(f"  Saves:      {sc:6,} slots @ 0x{so:06X}"
              f"  ({sc * layout['save_slot_size']:,} B, per-game)")
    else:
        print(f"  Saves:      none (no remaining space)")
    print('=' * 70)


# ---------------------------------------------------------------------------
# Image assembly
# ---------------------------------------------------------------------------

def build_flash_image(firmware_path, game_path, output_path, game_id, game_name):
    """Read inputs, validate, assemble, write."""

    # --- read ---
    print(f"\nReading firmware: {firmware_path}")
    fw_data = firmware_path.read_bytes()
    print(f"  {len(fw_data):,} bytes ({len(fw_data) / 1024:.1f} KB)")

    games = []
    if game_path and game_path.exists():
        print(f"Reading game:     {game_path}")
        gdata = game_path.read_bytes()
        print(f"  {len(gdata):,} bytes ({len(gdata) / 1024:.1f} KB)")
        games.append({'data': gdata, 'id': game_id, 'name': game_name})

    total_gd = sum(len(g['data']) for g in games)
    layout   = calculate_layout(len(fw_data), total_gd, num_games=1)

    # Attach per-game save info to each game dict for create_toc()
    for game in games:
        game['saves_offset']    = layout['per_game_saves_offset']
        game['save_slot_count'] = layout['per_game_save_slot_count']

    # --- validate ---
    if len(fw_data) > layout['firmware_reserved']:
        print(f"ERROR: Firmware ({len(fw_data):,} B) > reserved ({layout['firmware_reserved']:,} B)")
        sys.exit(1)

    if total_gd > layout['games_available']:
        print(f"ERROR: Games ({total_gd:,} B) > available ({layout['games_available']:,} B)")
        sys.exit(1)

    # --- assemble (0xFF = erased flash) ---
    print(f"\nBuilding flash image ({UFM_TOTAL:,} bytes)...")
    image = bytearray(b'\xff' * UFM_TOTAL)

    image[0:METADATA_SIZE] = create_metadata(layout, total_gd, platform='max10')
    print(f"  Metadata @ 0x{0:06X}")

    off = layout['firmware_offset']
    image[off:off + len(fw_data)] = fw_data
    print(f"  Firmware @ 0x{off:06X}")

    if games:
        toc  = create_toc(layout, games)
        toff = layout['toc_offset']
        image[toff:toff + len(toc)] = toc
        print(f"  TOC      @ 0x{toff:06X}")

        goff = layout['games_data_offset']
        for g in games:
            image[goff:goff + len(g['data'])] = g['data']
            print(f"  Game     @ 0x{goff:06X}  \"{g['name']}\"")
            goff += len(g['data'])

    # --- write ---
    output_path.write_bytes(image)
    print_layout(layout, len(fw_data), total_gd, games)
    print(f"Wrote: {output_path}")

    # side outputs
    layout_json = output_path.parent / 'max10_layout.json'
    with open(layout_json, 'w') as f:
        json.dump(layout, f, indent=2)
    print(f"Layout JSON: {layout_json}")

    generate_flash_layout_h(layout, output_path.parent)


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description='Build MAX10 XIP flash image (no bootloader)')
    parser.add_argument('--firmware', required=True,
                        help='Firmware binary (linked @ 0x80000100)')
    parser.add_argument('--game',
                        help='Raw game file (.z3)')
    parser.add_argument('--game-id',   default='game',
                        help='Game ID for TOC entry')
    parser.add_argument('--game-name', default='Game',
                        help='Display name for TOC entry')
    parser.add_argument('--output',
                        help='Output flash image (required unless --layout-only)')
    parser.add_argument('--layout-only', action='store_true',
                        help='Print layout and exit; do not write image')
    args = parser.parse_args()

    firmware = Path(args.firmware)
    if not firmware.exists():
        print(f"ERROR: Firmware not found: {firmware}")
        sys.exit(1)

    game = Path(args.game) if args.game else None
    if game and not game.exists():
        print(f"WARNING: Game not found: {game}  (continuing without game)")
        game = None

    if args.layout_only:
        fw_data = firmware.read_bytes()
        gd_data = game.read_bytes() if game else b''
        layout  = calculate_layout(len(fw_data), len(gd_data), num_games=1)
        games   = [{'data': gd_data, 'id': args.game_id, 'name': args.game_name}] if game else []
        print_layout(layout, len(fw_data), len(gd_data), games)
        return

    if not args.output:
        print("ERROR: --output is required (use --layout-only to just print layout)")
        sys.exit(1)

    build_flash_image(firmware, game, Path(args.output), args.game_id, args.game_name)


if __name__ == '__main__':
    main()
